#include "3-calc.h"

/**
 * op_add - add two integers.
 * @a: The first int.
 * @b: the second int.
 * Return: sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers.
 * @a: The first int.
 * @b: the second int.
 * Return: difference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers.
 * @a: The first int.
 * @b: the second int.
 * Return: product.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers.
 * @a: The first int.
 * @b: the second int.
 * Return: product of division.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two integers.
 * @a: The first int.
 * @b: the second int.
 * Return: remain after division.
 */
 int op_mod(int a, int b)
{
	return (a % b);
}
